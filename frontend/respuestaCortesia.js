function obtenerRespuestaCortesia(pregunta) {
    pregunta = pregunta.toLowerCase().trim();
    for (let key in respuestasCortesia) {
        if (pregunta.includes(key)) {
            const respuestas = respuestasCortesia[key];
            return respuestas[Math.floor(Math.random() * respuestas.length)];
        }
    }
    return null;
}
